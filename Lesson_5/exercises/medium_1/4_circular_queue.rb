class CircularQueue
  def initialize(queue_size)
    @queue = []
    @max_size = queue_size
  end

  def enqueue(object)
    @queue.shift if @queue.size == @max_size
    @queue.push(object)
  end

  def dequeue
    @queue.shift
  end
end

